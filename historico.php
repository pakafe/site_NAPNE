NAPNE (Núcleo de Atendimento às Pessoas com Necessidades Especiais)
O Núcleo de Apoio às Pessoas com Necessidades Específicas (NAPNE) é uma proposição da Secretaria de Educação Tecnológica e Profissional do Ministério da Educação (SETEC/MEC), através do Programa Tecnologia, Educação, Cidadania e Profissionalização para Pessoas com Necessidades Específicas (Programa TECNEP). O objetivo da iniciativa é consolidar uma política de educação inclusiva nas Instituições Federais de Ensino, atendendo o propósito da inclusão escolar, atuando diretamente no contexto escolar, disseminando conceitos, divulgando experiências e sensibilizando as comunidades escolares para a questão das necessidades específicas.

(Fonte: SETEC/MEC/TECNEP)

